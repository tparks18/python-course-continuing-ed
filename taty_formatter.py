me = ['data analyst', 'reader', 'mentor']
future_me = ['machine learning engineer', 'author', 'speaker']
past_me = ['student', 'intern', 'researcher']

def formatter(lst):
    #this function loops through a list and prints each item on a new line
    for i in lst:
        print(i)
    
print(formatter(me))
print(formatter(future_me))
print(formatter(past_me))